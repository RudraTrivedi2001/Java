package com.BasicConcepts.java.in;

import java.util.*;
public class TableUsingLoop {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Type a no.");
        int n = sc.nextInt();
        for(int i = 1; i<11; i++){
            System.out.println(n*i);
        }
    }}

